    private void jBtnConfirmarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnConfirmarActionPerformed
         //cria o bean
        Ebm_Usuarios usuarios = new Ebm_Usuarios();
        //coloca dados no usuraio
        int codigo = Integer.valueOf(jTxtCodigo.getText());
        usuarios.setIdusuarios(codigo);
        usuarios.setNome(jTxtNome.getText());
        usuarios.setApelido(jTxtApelido.getText());
        usuarios.setCpf(jFmtCpf.getText());
//        usuarios.setDataNascimento(jFmtDtn.getText());
        usuarios.setSenha(jPwfSenha.getText());
        usuarios.setNivel(jCboNivel.getSelectedIndex());
        
        if(jChbAtivo.isSelected()){
            usuarios.setAtivo("S");
        }
        else{
            usuarios.setAtivo("N");
        }
        
        UsuariosDAO usuario = new UsuariosDAO();
        
        usuario.insert(usuarios);

        habilitarComponentes(false);
        jBtnAlterar.grabFocus();
    }//GEN-LAST:event_jBtnConfirmarActionPerformed

