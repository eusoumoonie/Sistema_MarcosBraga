        jTxtNome.setEnabled(hab);
        jCboNivel.setEnabled(hab);
        jChbAtivo.setEnabled(hab);
        jFmtCpf.setEnabled(hab);
        jFmtData.setEnabled(hab);
        jPwfSenha.setEnabled(hab);
